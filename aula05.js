//loop for
for(let i = 0; i < 5; i++){
    console.log("Numero: " + i);
}

for(let i = 1; i <= 6; i++){
    console.log("Numero: " + i);
}
console.log("=====================================================")

//loop while
let count = 0
while(count < 5){
    console.log("Contando : " + count);
    count ++;
}

console.log("=====================================================")

//loop do...while
let check = 10
do {
    console.log("Checando : " + check);
    check ++;   
} while(check < 5);